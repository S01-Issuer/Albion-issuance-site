// The claimer and the matching CSV row (index 81) from the order's pinned CSV.
const OWNER = "0x8f6bF4A948Af2Fc74eE34982C4435a7C013D1A52";
const ORDER_HASH =
  "0xf1c6dbb3d1558926f5059610e991216cdac21b26b23102f2e789b8846cdf0bba";

const V6_SOURCE: OrderbookSource = {
  address: "0xb05D73E6BCc26AEB5b67Ff68C6E9C6151073e3cE",
  version: "v6",
  subgraphUrls: [],
  contextEventTopic:
    "0x4cb6e22a3e7e651d7cf0376cff48f20f5007a54147777865be7f5f6c38c50f4a",
  amountEncoding: "float",
  claimable: true,
};

function contextLog(): HypersyncResult {
  return {
    block_number: "47078524",
    log_index: "0",
    transaction_index: "0",
    transaction_hash:
      "0x0bb856c870f60d95e3d753b1c9b52025c475b886bc44e93483d08677c3b06af1",
    data: V6_CONTEXT_LOG_DATA,
    address: V6_SOURCE.address.toLowerCase(),
    topic0: V6_SOURCE.contextEventTopic,
    timestamp: 1765000000,
  };
}

const CSV_ROWS = [
  { index: "81", address: OWNER.toLowerCase(), amount: "1023586000000000000" },
  { index: "82", address: OWNER.toLowerCase(), amount: "55000000000000000" },
];

async function runSort(orderHash: string) {
  return sortClaimsData(
    CSV_ROWS,
    [], // trades
    OWNER,
    "Wressle-1",
    undefined,
    "0xf836a500910453A397084ADe41321ee20a5AAde1",
    orderHash,
    "ALB-WR1-R1",
    47065011,
    [contextLog()],
    V6_SOURCE,
    [],
  );
}

describe("v6 claimed-detection scoping (ContextV2 col[1][0] = order hash)", () => {
  it("marks a claimed row as claimed when the log's order hash matches this order", async () => {
    const result = await runSort(ORDER_HASH);

    expect(result.claimedCount).toBe(1);
    expect(result.claimedCsv[0]?.index).toBe("81");
    // The just-claimed amount must leave "unclaimed" — this is the bug where a
    // successful claim still shows as available to claim.
    expect(result.unclaimedCount).toBe(1);
    expect(result.unclaimedCsv[0]?.index).toBe("82");
    expect(result.totalClaimedAmount).toBe("1023586000000000000");
    expect(result.claims[0]?.txHash).toBe(
      "0x0bb856c870f60d95e3d753b1c9b52025c475b886bc44e93483d08677c3b06af1",
    );
  });

  it("does NOT mark rows claimed for a different order (cross-order scoping)", async () => {
    const otherOrder =
      "0xb0ffec19ad51000000000000000000000000000000000000000000000000beef";
    const result = await runSort(otherOrder);

    expect(result.claimedCount).toBe(0);
    expect(result.unclaimedCount).toBe(2);
  });
});
